#pragma once

#include <Arduino.h>

/*
  LCDWiki 2.8inch ESP32-S3 Display, touch SKU ES3C28P.

  Arduino board target used in TamaFi:
  esp32:esp32:esp32s3:FlashSize=16M,PSRAM=opi,PartitionScheme=app3M_fat9M_16MB
*/

#define LCDWIKI_ES3C28P_BOARD_NAME "LCDWiki ESP32-S3 ES3C28P"

// Memory profile.
#define LCDWIKI_ES3C28P_FLASH_MB 16
#define LCDWIKI_ES3C28P_PSRAM_MB 8
#define LCDWIKI_ES3C28P_PSRAM_OPI 1

// Display.
#define LCDWIKI_ES3C28P_LCD_DRIVER_ILI9341 1
#define LCDWIKI_ES3C28P_SCREEN_WIDTH 240
#define LCDWIKI_ES3C28P_SCREEN_HEIGHT 320
#define LCDWIKI_ES3C28P_DISPLAY_ROTATION 0
#define LCDWIKI_ES3C28P_DISPLAY_INVERT_COLORS 1
#define LCDWIKI_ES3C28P_SPI_FREQUENCY 27000000UL

#define LCDWIKI_ES3C28P_LCD_CS 10
#define LCDWIKI_ES3C28P_LCD_DC 46
#define LCDWIKI_ES3C28P_LCD_SCK 12
#define LCDWIKI_ES3C28P_LCD_MOSI 11
#define LCDWIKI_ES3C28P_LCD_MISO 13
#define LCDWIKI_ES3C28P_LCD_RST -1
#define LCDWIKI_ES3C28P_LCD_BL 45
#define LCDWIKI_ES3C28P_LCD_BL_ON HIGH
#define LCDWIKI_ES3C28P_LCD_BL_OFF LOW

// Square UI helper used by TamaFi. Use full 240x320 for normal portrait apps.
#define LCDWIKI_ES3C28P_TAMAFI_UI_WIDTH 240
#define LCDWIKI_ES3C28P_TAMAFI_UI_HEIGHT 240
#define LCDWIKI_ES3C28P_TAMAFI_TOUCH_ACTIVE_X_MIN 0
#define LCDWIKI_ES3C28P_TAMAFI_TOUCH_ACTIVE_Y_MIN 0
#define LCDWIKI_ES3C28P_TAMAFI_TOUCH_ACTIVE_X_MAX 240
#define LCDWIKI_ES3C28P_TAMAFI_TOUCH_ACTIVE_Y_MAX 240

// Touch.
#define LCDWIKI_ES3C28P_TOUCH_DRIVER_FT6336 1
#define LCDWIKI_ES3C28P_TOUCH_ADDR 0x38
#define LCDWIKI_ES3C28P_TOUCH_SDA 16
#define LCDWIKI_ES3C28P_TOUCH_SCL 15
#define LCDWIKI_ES3C28P_TOUCH_RST 18
#define LCDWIKI_ES3C28P_TOUCH_INT 17
#define LCDWIKI_ES3C28P_TOUCH_I2C_FREQUENCY 400000UL

// One addressable RGB LED.
#define LCDWIKI_ES3C28P_RGB_PIN 42
#define LCDWIKI_ES3C28P_RGB_COUNT 1

// Audio.
#define LCDWIKI_ES3C28P_AUDIO_CODEC_ES8311 1
#define LCDWIKI_ES3C28P_AUDIO_ADDR 0x18
#define LCDWIKI_ES3C28P_AUDIO_EN 1
#define LCDWIKI_ES3C28P_AUDIO_EN_ACTIVE LOW
#define LCDWIKI_ES3C28P_AUDIO_MCLK 4
#define LCDWIKI_ES3C28P_AUDIO_BCLK 5
#define LCDWIKI_ES3C28P_AUDIO_LRCK 7
// Validated with a direct ES8311 tone probe: ESP32-S3 TX data to ES8311 DIN is GPIO8.
// Some LCDWiki material names GPIO6/GPIO8 from the codec side, which is easy to invert.
#define LCDWIKI_ES3C28P_AUDIO_DOUT 8
#define LCDWIKI_ES3C28P_AUDIO_DIN 6
#define LCDWIKI_ES3C28P_AUDIO_SAMPLE_RATE 16000

// MicroSD SDIO.
#define LCDWIKI_ES3C28P_SD_CLK 38
#define LCDWIKI_ES3C28P_SD_CMD 40
#define LCDWIKI_ES3C28P_SD_D0 39
#define LCDWIKI_ES3C28P_SD_D1 41
#define LCDWIKI_ES3C28P_SD_D2 48
#define LCDWIKI_ES3C28P_SD_D3 47

// System and expansion.
#define LCDWIKI_ES3C28P_BOOT_PIN 0
#define LCDWIKI_ES3C28P_UART0_RX 43
#define LCDWIKI_ES3C28P_UART0_TX 44
#define LCDWIKI_ES3C28P_BATTERY_ADC 9
#define LCDWIKI_ES3C28P_EXPAND_0 2
#define LCDWIKI_ES3C28P_EXPAND_1 3
#define LCDWIKI_ES3C28P_EXPAND_2 14
#define LCDWIKI_ES3C28P_EXPAND_3 21
